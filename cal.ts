function sum(num1: number, num2: number) {
    
    return num1+num2;
}
function sub(num1: number, num2: number) {
     
    return num1-num2;
}

function multiply(num1: number, num2: number) {
     
    return num1*num2;
}
function divide(num1: number, num2: number) {
     
    return num1/num2;
}
export { divide };
export { sum };
export { sub };
export { multiply };
